/*
*********************************************************************************************************
*	函 数 名: I2C_Delay
*	功能说明: I2C总线位延迟，最快400KHz
*	形    参：无
*	返 回 值: 无
*********************************************************************************************************
*/
static void I2C_Delay(void)
{
	uint8_t i;

	/*　
	 	下面的时间是通过逻辑分析仪测试得到的。
    工作条件：CPU主频72MHz ，MDK编译环境，1级优化
  
		循环次数为10时，SCL频率 = 205KHz 
		循环次数为7时，SCL频率 = 347KHz， SCL高电平时间1.5us，SCL低电平时间2.87us 
	 	循环次数为5时，SCL频率 = 421KHz， SCL高电平时间1.25us，SCL低电平时间2.375us 
	*/
	for (i = 0; i < 10; i++);
}

/*
*********************************************************************************************************
*	函 数 名: SW_I2C_Start
*	功能说明: CPU发起I2C总线启动信号
*	形    参：无
*	返 回 值: 无
*********************************************************************************************************
*/
void SW_I2C_Start(void)
{
	/* 当SCL高电平时，SDA出现一个下跳沿表示I2C总线启动信号 */
	SW_I2C_SDA_1();
	SW_I2C_SCL_1();
	I2C_Delay();
	SW_I2C_SDA_0();
	I2C_Delay();
	SW_I2C_SCL_0();
	I2C_Delay();
}

/*
*********************************************************************************************************
*	函 数 名: SW_I2C_Start
*	功能说明: CPU发起I2C总线停止信号
*	形    参：无
*	返 回 值: 无
*********************************************************************************************************
*/
void SW_I2C_Stop(void)
{
	/* 当SCL高电平时，SDA出现一个上跳沿表示I2C总线停止信号 */
	SW_I2C_SDA_0();
	SW_I2C_SCL_1();
	I2C_Delay();
	SW_I2C_SDA_1();
}

/*
*********************************************************************************************************
*	函 数 名: SW_I2C_SendByte
*	功能说明: CPU向I2C总线设备发送8bit数据
*	形    参：_ucByte ： 等待发送的字节
*	返 回 值: 无
*********************************************************************************************************
*/
void SW_I2C_SendByte(uint8_t _ucByte)
{
	uint8_t i;

	/* 先发送字节的高位bit7 */
	for (i = 0; i < 8; i++)
	{		
		if (_ucByte & 0x80)
		{
			SW_I2C_SDA_1();
		}
		else
		{
			SW_I2C_SDA_0();
		}
		I2C_Delay();
		SW_I2C_SCL_1();
		I2C_Delay();	
		SW_I2C_SCL_0();
		if (i == 7)
		{
			 SW_I2C_SDA_1(); // 释放总线
		}
		_ucByte <<= 1;	/* 左移一个bit */
		I2C_Delay();
	}
}

/*
*********************************************************************************************************
*	函 数 名: SW_I2C_ReadByte
*	功能说明: CPU从I2C总线设备读取8bit数据
*	形    参：无
*	返 回 值: 读到的数据
*********************************************************************************************************
*/
uint8_t SW_I2C_ReadByte(void)
{
	uint8_t i;
	uint8_t value;

	/* 读到第1个bit为数据的bit7 */
	value = 0;
	for (i = 0; i < 8; i++)
	{
		value <<= 1;
		SW_I2C_SCL_1();
		I2C_Delay();
		if (SW_I2C_SDA_READ())
		{
			value++;
		}
		SW_I2C_SCL_0();
		I2C_Delay();
	}
	return value;
}

/*
*********************************************************************************************************
*	函 数 名: SW_I2C_WaitAck
*	功能说明: CPU产生一个时钟，并读取器件的ACK应答信号
*	形    参：无
*	返 回 值: 返回0表示正确应答，1表示无器件响应
*********************************************************************************************************
*/
uint8_t SW_I2C_WaitAck(void)
{
	uint8_t re;

	SW_I2C_SDA_1();	/* CPU释放SDA总线 */
	I2C_Delay();
	SW_I2C_SCL_1();	/* CPU驱动SCL = 1, 此时器件会返回ACK应答 */
	I2C_Delay();
	if (SW_I2C_SDA_READ())	/* CPU读取SDA口线状态 */
	{
		re = 1;
	}
	else
	{
		re = 0;
	}
	SW_I2C_SCL_0();
	I2C_Delay();
	return re;
}

/*
*********************************************************************************************************
*	函 数 名: I2C_Ack
*	功能说明: CPU产生一个ACK信号
*	形    参：无
*	返 回 值: 无
*********************************************************************************************************
*/
void I2C_Ack(void)
{
	SW_I2C_SDA_0();	/* CPU驱动SDA = 0 */
	I2C_Delay();
	SW_I2C_SCL_1();	/* CPU产生1个时钟 */
	I2C_Delay();
	SW_I2C_SCL_0();
	I2C_Delay();
	SW_I2C_SDA_1();	/* CPU释放SDA总线 */
}

/*
*********************************************************************************************************
*	函 数 名: SW_I2C_NAck
*	功能说明: CPU产生1个NACK信号
*	形    参：无
*	返 回 值: 无
*********************************************************************************************************
*/
void SW_I2C_NAck(void)
{
	SW_I2C_SDA_1();	/* CPU驱动SDA = 1 */
	I2C_Delay();
	SW_I2C_SCL_1();	/* CPU产生1个时钟 */
	I2C_Delay();
	SW_I2C_SCL_0();
	I2C_Delay();	
}

